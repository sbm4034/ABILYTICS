public class NoiVersion {
            public String email;
            public String noi;
            public String cart_id;
            public String product_code;
            public String price;
            public String main_product_id;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNoi() {
        return noi;
    }

    public void setNoi(String noi) {
        this.noi = noi;
    }

    public String getCart_id() {
        return cart_id;
    }

    public void setCart_id(String cart_id) {
        this.cart_id = cart_id;
    }

    public String getProduct_code() {
        return product_code;
    }

    public void setProduct_code(String product_code) {
        this.product_code = product_code;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getMain_product_id() {
        return main_product_id;
    }

    public void setMain_product_id(String main_product_id) {
        this.main_product_id = main_product_id;
    }
}
